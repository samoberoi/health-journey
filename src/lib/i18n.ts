const translations: Record<Language, TranslationKeys> = { en, hi, ta, te, kn, ml, mr, bn, gu, pa, or: or_, as: as_ };

export function t(lang: Language, key: keyof TranslationKeys): string {
  return translations[lang]?.[key] ?? en[key] ?? key;
}

export function getGreeting(lang: Language): string {
  const h = new Date().getHours();
  if (h < 12) return t(lang, "goodMorning");
  if (h < 17) return t(lang, "goodAfternoon");
  return t(lang, "goodEvening");
}
